namespace SDP.CSharp.Excercise4.AnonymousTypesAndLambdas.Model
{
    /// <summary>
    /// Represents a single Person
    /// </summary>
    class Person
    {
        /// <summary>
        /// Gets or sets a person name
        /// </summary>
        public string Name
        {
            get; set;
        }

        /// <summary>
        /// Gets or sets person's city of living
        /// </summary>
        public string City
        {
            get; set;
        }

        /// <summary>
        /// Gets or sets person's sign-in date
        /// </summary>
        public string SignInDate
        {
            get; set;
        }

        /// <summary>
        /// Gets or sets person's company
        /// </summary>
        public string Company
        {
            get; set;
        }

        /// <summary>
        /// Gets or sets person's number of vowels in name
        /// </summary>
        public int NumberOfVowels
        {
            get; set;
        }
    }
}
